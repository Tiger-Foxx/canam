    private void themeCheckActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_themeCheckActionPerformed
        // TODO add your handling code here:
        
        // Cette fonction change le th√®me de couleur de l'interface graphique en fonction de l'√©tat du composant themeCheck
        // Si themeCheck est s√©lectionn√©, la fonction applique le th√®me FlatMacDarkLaf, qui est un th√®me sombre
        // Sinon, la fonction applique le th√®me FlatMacLightLaf, qui est un th√®me clair
        // La fonction utilise la biblioth√®que FlatLaf pour g√©rer les th√®mes de couleur
        // La fonction utilise √©galement la classe FlatAnimatedLafChange pour cr√©er une animation de transition entre les th√®mes
        // La fonction utilise la m√©thode invokeLater de la classe EventQueue pour ex√©cuter le changement de th√®me dans le thread de l'interface graphique
        
        if (themeCheck.isSelected()) {
            EventQueue.invokeLater(new Runnable(){
            
            @Override
            public void run(){
                FlatAnimatedLafChange.showSnapshot();
                FlatMacDarkLaf.setup();
                FlatLaf.updateUI();
                FlatAnimatedLafChange.hideSnapshotWithAnimation();
                
            }
            
        });
            
        } else {
            
            EventQueue.invokeLater(new Runnable(){
            
            @Override
            public void run(){
               FlatAnimatedLafChange.showSnapshot();
                FlatMacLightLaf.setup();
                FlatLaf.updateUI();
                FlatAnimatedLafChange.hideSnapshotWithAnimation();
                
            }
            
        });
        }
        
        
       
    }//GEN-LAST:event_themeCheckActionPerformed

