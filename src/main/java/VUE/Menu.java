    private void SaveAsPngActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SaveAsPngActionPerformed
         // Cette fonction permet de sauvegarder l'image g√©n√©r√©e dans le panel de dessin sous format image (png)
        // La fonction utilise la classe JFileChooser pour afficher une bo√Æte de dialogue qui permet √† l'utilisateur de choisir le nom et l'emplacement du fichier image
        // La fonction utilise la classe ImageFilter pour filtrer les fichiers qui ne sont pas des images
        // La fonction utilise la classe BufferedImage pour cr√©er une image √† partir du contenu du panel de dessin
        // La fonction utilise la classe Graphics pour dessiner la forme sur l'image
        // La fonction utilise la classe ImageIO pour √©crire l'image dans le fichier choisi
        // La fonction g√®re les exceptions de type IOException qui peuvent survenir lors de la cr√©ation ou de l'√©criture du fichier image
        
        JFileChooser fileChooser = new JFileChooser();
        fileChooser.setDialogTitle("ENTREZ LE NOM DE VOTRE FICHIER IMAGE");
        fileChooser.setFileFilter(new ImageFilter());
        if (fileChooser.showSaveDialog(PanelDraw) == JFileChooser.APPROVE_OPTION) {
          File file = fileChooser.getSelectedFile();
            try {
                //creation d'un bjet buffered image a partir du contenu du Panel de dessin
                BufferedImage image= (BufferedImage)PanelDraw.createImage(PanelDraw.getWidth(),PanelDraw.getHeight());
                Graphics g=image.getGraphics();
                PanelDraw.paint(g); // ce code est l√† pour que l'image g√©n√©r√©e ne soit pas vide , on redessine la figure sur e panelDraw
                ImageIO.write(image, "png", file);
            } catch (IOException ex) {
                Logger.getLogger(Menu.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_SaveAsPngActionPerformed

